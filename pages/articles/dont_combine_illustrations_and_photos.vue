        <h1>Don't combine illustrations and photos</h1>

        <p>Oddly I’ve never seen this topic written about but it will save you so much time! Don't use both illustrations AND photos in your website design. Pick one and stick with it.</p>

<h2>Why does it matter?</h2>
<p>It’s all about cognitive load. Using both photos and illustrations makes your user think too much.
Pretend for a moment that you’re bilingual and you’re reading an article where every odd numbered paragraph is written in English and every even numbered paragraph is written in Spanish.
Even though you speak both languages you would still need to slow down slightly to make sense of the information.
Using both photographs and illustrations in your design does the same thing. It slows down the user. They may not even be conscious that it’s happening but their subconscious will feel it.
Good design is about making things only as complex as needed and no more. And this rule helps with that. It’s a forcing function that helps remove future decisions about what imagery to use during the design process.
Here are some websites that use only* illustrations or photographs:</p>



